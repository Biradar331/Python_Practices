# python program to display a multiplication table using for loop

n=int(input("enter a table number:"))
for i in range (1,11):
    print(n,"×", i, "=", n*i)


# python program to display a multiplication tale using while loop

n=int(input("enter a multiplication table number:"))
i=1
while i <= 10:
    print(n,"×", i, "=", n*i)
    i=i+1
    
